"""OD 解析通用文本模式。

这里集中维护“口语位置前缀、路线意图后缀、疑问噪声、方向动作词”等语义模式。
normalizer 和 guard 复用同一组模式，避免在多个文件里分别堆砌 token 列表。
"""

from __future__ import annotations

import re

# 用户问题外层包装，例如日志或 prompt 中的“用户问题：”。
WRAPPER_PREFIX_PATTERN = re.compile(r"^\s*(?:latest_user_message\s*:\s*)?(?:用户问题\s*[:：]\s*)+")

# 空白归一化，OD 中文问句里空白通常只是格式噪声。
SPACE_PATTERN = re.compile(r"\s+")

# 起点片段常见前缀：
# - 主体：我/本人/当前车
# - 当前状态：现在/目前/当前/此刻/已经
# - 位置关系：在/位于/处在/到达/从/由
ENDPOINT_PREFIX_PATTERN = re.compile(
    r"^(?:"
    r"(?:我|本人|当前车)?(?:现在|目前|当前|此刻|已经)?(?:正?在|位于|处在|到达)"
    r"|当前位置(?:在|是)?"
    r"|(?:从|由)"
    r")+"
)

# 泛化地点候选可能会从非地点词开始匹配，例如“请问申嘉湖高速”“今天湖州服务区”。
# 这类词不表达 OD 关系，只是口语外壳或查询动作，应该在候选入选前剥离。
CANDIDATE_PREFIX_PATTERN = re.compile(
    r"^(?:"
    r"(?:你好|麻烦问一下|帮我看看|请帮我查下|请帮我查一下|请问|查一下|导航)"
    r"|(?:今天|现在|这会儿|当前|目前|此刻)"
    r"|(?:我)?(?:已经)?(?:到达|达)"
    r"|[,，。；;、\s]+"
    r")+"
)

# 路线动作词。既可用于端点后缀清理，也可用于 guard 判断“杭州去”这类脏参数。
ROUTE_ACTION_SUFFIX_PATTERN = re.compile(r"(?:前往|去往|开往|过去|往|去|到|回)$")

# 目的地片段常见尾部意图：
# “杭州还要多久”“上海啥时候”“杭州怎么走”“杭州堵不堵”等都应该清成地点本身。
ENDPOINT_SUFFIX_PATTERN = re.compile(
    r"(?:"
    r"(?:还有|还要|需要)?(?:多少|多长)?时间(?:能到|到达)?"
    r"|(?:还有|还要|需要)?多久(?:能到|到达)?"
    r"|(?:几时|啥时候|什么时候)(?:能到|到达)?"
    r"|(?:能到|到达)"
    r"|(?:怎么|如何)(?:走|去)"
    r"|(?:堵不堵|堵吗|拥堵吗|路况如何|路况)"
    r"|方向"
    r"|要"
    r")$"
)

# guard 使用的非地点语义噪声。若端点仍包含这些词，说明边界没有切干净。
DIRTY_ENDPOINT_PATTERN = re.compile(
    r"(?:"
    r"用户问题|latest_user_message"
    r"|我(?:现在|目前|正在|在)"
    r"|当前位置|当前|正在|目前"
    r"|还有|还要|多少时间|多长时间|多久"
    r"|几时|啥时候|什么时候"
    r"|怎么|如何|堵不堵|堵吗|拥堵|路况"
    r"|[?？]"
    r")"
)
